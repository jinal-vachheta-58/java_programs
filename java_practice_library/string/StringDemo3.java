//Example 15.3: Collection of strings
// Demonstrate String arrays.

class StringDemo3 {
public static void main(String args[]) {
String str[] = { "one", "two", "three" };
for(int i=0; i<str.length; i++)
System.out.println("str[" + i + "]: " +
str[i]);

}
}

/*

str[0]: one
str[1]: two
str[2]: three

*/